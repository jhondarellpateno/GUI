    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        String oid = order.getText();
        String pid = txtID.getText();
        String name = txtName.getText();
        String cat = txtCategory.getText();
        String si = txtSize.getText();
        String col = txtColor.getText();
        String pri = txtPrice.getText();
        String qty = jSpinner1.getValue().toString();

        config conf = new config();

        String sql = "UPDATE tbl_order SET i_id = ?, o_name = ?, o_category = ?, o_size = ?, o_color = ?, o_price = ?, o_quantity = ? WHERE o_id = ?";
        conf.updateRecord(sql, pid, name, cat, si, col, pri, qty, oid);

        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to update Order #" + oid + "?", "Update_warning", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {

            order.setText("");
            txtID.setText("");
            txtName.setText("");
            txtCategory.setText("");
            txtSize.setText("");
            txtColor.setText("");
            txtPrice.setText("");
            jSpinner1.setValue("");

            displayOrd();
            displayProd();
            JOptionPane.showMessageDialog(null, "Order " + oid + " has been updated in the database!");
        }

        Managerdashboard back = new Managerdashboard();
        back.setLocationRelativeTo(null);
        back.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jToggleButton1ActionPerformed

