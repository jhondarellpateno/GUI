    private void productsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productsMouseClicked
        DefaultTableModel model = (DefaultTableModel) products.getModel();
        int selectedRow = products.getSelectedRow();

        txtID.setText(model.getValueAt(selectedRow, 0).toString());
        txtName.setText(model.getValueAt(selectedRow, 1).toString());
        txtCategory.setText(model.getValueAt(selectedRow, 2).toString());
        txtSize.setText(model.getValueAt(selectedRow, 3).toString());
        txtColor.setText(model.getValueAt(selectedRow, 4).toString());

        String rawPrice = model.getValueAt(selectedRow, 5).toString().replace("â‚±", "").replace(",", "");
        txtPrice.setText(rawPrice);

        String qtyString = model.getValueAt(selectedRow, 6).toString();
        int qtyInt = Integer.parseInt(qtyString);
        jSpinner1.setValue(qtyInt);
    }//GEN-LAST:event_productsMouseClicked

